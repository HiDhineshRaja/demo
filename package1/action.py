"""
This module have two methods
1. add
2. subtract
"""

"""
@add add two munber
"""
def add(a, b):
    """
    :param a: first interger number
    :param b: second integer number
    :return: add first and second number
    """
    return a + b


"""
@minus subtract two number
"""
def minus(a, b):
    """
    :param a: first interger number
    :param b: second integer number
    :return: subtract of second number from first
    """
    return a - b
